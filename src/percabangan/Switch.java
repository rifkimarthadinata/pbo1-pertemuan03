package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanjalan=3;
        switch(jalanjalan){
            case 1:
                System.out.println("urang bukah");
                break;
            case 2:
                System.out.println("urang acayan");
                break;
            case 3:
                System.out.println("Ubat Nyamuk");
                break;
            case 4:
                System.out.println("dubil dit");
                break;
            default:
                System.out.println("mahluk halus");
                break;

        }
    }
}
